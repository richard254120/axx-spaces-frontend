/** All campuses in a given county */
export const getUniversitiesByCounty = (county) =>
  kenyanUniversities.filter(
    (u) => u.county.toLowerCase() === county.toLowerCase()
  );

/** Search across name, location, county, and code */
export const searchUniversities = (query) => {
  const q = query.toLowerCase();
  return kenyanUniversities.filter(
    (u) =>
      u.name.toLowerCase().includes(q) ||
      u.location.toLowerCase().includes(q) ||
      u.county.toLowerCase().includes(q) ||
      u.code.toLowerCase().includes(q)
  );
};

/** Sorted list of counties that have at least one campus */
export const getCountiesWithUniversities = () =>
  [...new Set(kenyanUniversities.map((u) => u.county))].sort();